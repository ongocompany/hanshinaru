export function detectMeter(bodyZh) {
  const lines = String(bodyZh ?? '').split(/\r?\n/).map((line) => line.trim()).filter(Boolean);
  if (!lines.length) return null;
  const lengths = lines.map((line) => Array.from(line.replace(/[，。！？、；：\s]/g, '')).length);
  if (lengths.every((len) => len === 5)) return 5;
  if (lengths.every((len) => len === 7)) return 7;
  return null;
}

export function detectCategory(bodyZh, options = {}) {
  if (String(bodyZh ?? '').includes('兮')) return null;
  const meter = detectMeter(bodyZh);
  if (!meter) return null;
  const lineCount = String(bodyZh ?? '').split(/\r?\n/).map((line) => line.trim()).filter(Boolean).length;
  const prefix = meter === 5 ? '五言' : '七言';
  if (['qian-han', 'wei-jin', 'nanbei'].includes(options.eraSlug)) return `${prefix}古詩`;
  if (lineCount === 4) return `${prefix}絶句`;
  if (lineCount === 8) return `${prefix}律詩`;
  if (lineCount > 8) return `${prefix}古詩`;
  if (lineCount === 2) return `${prefix}聯句`;
  return `${prefix}古詩`;
}
